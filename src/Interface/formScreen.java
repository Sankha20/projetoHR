    private void clearInput() {
        txtCargo.setText(null);
        txtCodigo.setText(null);
        txtMaiorSalario.setText(null);
        txtMenorSalario.setText(null);
    }

    private Job selectJob() {
        DefaultTableModel table = (DefaultTableModel) tableJob.getModel();
        Job job = new Job();

        int row = tableJob.getSelectedRow();

        if (row == -1) {
            Tools.error("Selecione uma entrada válida na tabela.");
        } else {

            String codigo = (String) table.getValueAt(row, 0);
            String cargo = (String) table.getValueAt(row, 1);
            String menorSalario = (String) table.getValueAt(row, 2);
            String maiorSalario = (String) table.getValueAt(row, 3);

            menorSalario = Tools.parseNumber(menorSalario);
            maiorSalario = Tools.parseNumber(maiorSalario);

            BigDecimal salarioMin = Tools.parseMoney(menorSalario);
            BigDecimal salarioMax = Tools.parseMoney(maiorSalario);

            job = new Job(codigo, cargo, salarioMin, salarioMax);
        }

        return job;
    }

    private formJobViwer loadJobViwer(Job job) {
        formJobViwer.currentJob = job;

        formJobViwer jobViwer = new formJobViwer();
        jobViwer.setLocationRelativeTo(btnDeletar);
        jobViwer.setVisible(true);
        
        return jobViwer;
    }

    public void refreshJobTable() {
        ArrayList<Job> jobs = new ArrayList<>();
        jobs = Job.selectAllFromDatabase();

        DefaultTableModel table = (DefaultTableModel) tableJob.getModel();

        table.setRowCount(0);

        jobs.forEach((job) -> {
            table.addRow(new Object[]{
                job.getId(),
                job.getTitle(),
                Tools.moneyFormat(job.getMinSalary()),
                Tools.moneyFormat(job.getMaxSalary())});
        });

        int rowAmount = table.getRowCount();
        labelQuantidade.setText(String.valueOf(rowAmount));
    }

