    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        // TODO add your handling code here:
        String cargo = txtCargo.getText();
        String codigo = txtCodigo.getText();
        String menorSalario = txtMenorSalario.getText();
        String maiorSalario = txtMaiorSalario.getText();

        if (!cargo.isEmpty()
                && !codigo.isEmpty()
                && !menorSalario.isEmpty()
                && !maiorSalario.isEmpty()) {
            // Se nada estiver vazio

            // Ler Salario
            BigDecimal salarioMin = Tools.parseMoney(menorSalario);
            BigDecimal salarioMax = Tools.parseMoney(maiorSalario);

            if (salarioMin.intValue() == -1 || salarioMax.intValue() == -1) {
                Tools.error("Valores dos salários inválidos.");
            } else {
                Job job = new Job(codigo, cargo, salarioMin, salarioMax);

                if (job.insertIntoDatabase()) {
                    Tools.alert("Inserido com sucesso.");
                    refreshJobTable();
                } else {
                    Tools.error("Erro ao inserir no banco de dados.");
                }
            }
        } else {
            Tools.alert("Preencha todos os valores.");
        }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:

        Job job = selectJob();

        if (!job.getId().isEmpty()) {
            
            formJobViwer jobViwer = loadJobViwer(job);

            boolean confirm = Tools.confirm("Deseja mesmo deletar?");

            if (confirm) {
                jobViwer.dispose();
                boolean success = job.deleteFromDatabase();

                if (success) {
                    Tools.alert("Deletado com sucesso.");
                    refreshJobTable();
                    clearInput();
                } else {
                    Tools.error("Erro ao deletar.");
                }
            }
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

