    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:

        boolean confirm = Tools.confirm("Tem certeza que deseja alterar?");
        
        System.out.println(txtMaxSalary.getText() + " " + Tools.parseNumber(txtMaxSalary.getText()));
        System.out.println(txtMinSalary.getText() + " " + Tools.parseNumber(txtMinSalary.getText()));

        if (confirm) {
            String cargo = txtCargo.getText();
            String maxSalary = txtMaxSalary.getText();
            String minSalary = txtMinSalary.getText();
            
            
            
            maxSalary = Tools.parseNumber(maxSalary);
            minSalary = Tools.parseNumber(minSalary);
            
            System.out.println(maxSalary);
            System.out.println(minSalary);

            BigDecimal salarioMinimo = new BigDecimal(minSalary);
            BigDecimal salarioMaximo = new BigDecimal(maxSalary);

            currentJob.selfUpdate(cargo, salarioMinimo, salarioMaximo);
            boolean success = currentJob.updateDatabase();

            if (success) {
                Tools.alert("Alterado com sucesso");
                this.dispose();
            } else {
                Tools.error("Erro ao alterar.");
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

