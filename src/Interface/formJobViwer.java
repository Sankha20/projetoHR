    private void loadJob() {
        if (currentJob != null) {
            String id = String.valueOf(currentJob.getId());
            String title = String.valueOf(currentJob.getTitle());
            String minSalary = Tools.moneyFormat(currentJob.getMinSalary());
            String maxSalary = Tools.moneyFormat(currentJob.getMaxSalary());

            txtCodigo.setText(id);
            txtCargo.setText(title);
            txtMinSalary.setText(minSalary);
            txtMaxSalary.setText(maxSalary);
        }
    }

